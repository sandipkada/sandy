package Array;

public class IntSwap {

	public static void main(String[] args) {
		
		int a=80;
		int b=90;
		System.out.println("swapping the valuse of integer");
		
		int x;//declear
		x=a;//a=80 value is swapped in x; then a is empty now
		
		a=b;//b=90 value is swapped in a; then b is empty now
		
		b=x;//x=80 value is swapped in b; then x is empty now
		
		System.out.println(a);// value is a=90;
		System.out.println(b);// value is b=80;
		
		
		
		
		
		
	}
}
