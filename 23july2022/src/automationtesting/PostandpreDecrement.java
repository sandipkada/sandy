package automationtesting;

public class PostandpreDecrement { 
	
	public static void main(String[] args)
	{
		
		int a=67;
		System.out.println(a); // value of post decrement (a)=67
		System.out.println(a--);  //post decrement
		System.out.println(a); //value of pre decrement(a)=67
		System.out.println(--a);// value of post decrement
		
	}

}
